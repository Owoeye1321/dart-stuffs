void main() {
  //string
  String firstName = "Samuel";
  print("String: $firstName");

  //integer
  int age = 53;
  print("Integer: $age");

  //double
  double cgpa = 2.79;
  print("Double: $cgpa");

  //boolean
  bool status = false;
  print("Boolean: $status");

  //dynamic
  dynamic fullName = "Owoeye Samuel";
  print("Dynamic: $fullName");
}
